int dx[] = {1, 1, 1, 0, -1, -1, -1, 0};
int dy[] = {1, 0, -1, -1, -1, 0, 1, 1};
int arr[101][101];
int n, m;

bool backtrack (int x, int y, int v) {
    if (arr[y][x] != 0) return false;

    for (int i = 0; i < 8; i++) {
        int now_x = x + dx[i];
        int now_y = y + dy[i];

        if (arr[now_y][now_x] != 0) continue;

        if (now_x < 0 or now_x >= m or now_y < 0 or now_y >= n) continue;

        if (backtrack (now_x, now_y, v + 1) ) return true;
    }

    return false;
}

int main() {
    __FastIO;
    int x, y;
    cin >> n >> m >> x >> y;

    for (int i = 0; i < x; i++) {
        arr[0][i] = -1;
    }

    for (int i = 0; i < y; i++) {
        arr[n - 1][m - i - 1] = -1;
    }

    int k  = backtrack (n - 1, 0, 1);
    cout << k << endl;
    return 0;
}


